              chol_ldl  = "mg/dL",
              egfr_2021  = "ml/min/1.73m2",
              sbp  = "mm Hg",
              dbp  = "mm Hg",
              trigs  = "mg/dL") %>%
    set_divby_modeling(lpa_nmol = 25,
                       lpa_mgdl = 25,
                       age = 10,
                       bmi = 5,
                       chol_total = 20,
                       chol_hdl = 10,
                       sbp = 15)

}
