  dd %>%
    set_labels(risk_horizon  = "time of risk prediction",
               overall  = "Overall",
               study  = "Study",
               lpa_nmol  = "Lp(a)",
               lpa_mgdl  = "Lp(a)",
               lpa_nmol_log  = "Log Lp(a)",
               lpa_mgdl_log  = "Log Lp(a)",
               status_ascvd  = "ASCVD event",
               time_ascvd  = "Time to ASCVD",
               status_cvd  = "CVD event",
               time_cvd  = "Time to CVD",
               status_chd  = "CHD event",
               time_chd  = "Time to CHD",
               cvd_risk_10yr  = "10-year CVD risk",
               cvd_risk_30yr  = "30-year CVD risk",
               chd_risk_10yr  = "10-year CHD risk",
               chd_risk_30yr  = "30-year CHD risk",
               ascvd_risk_10yr  = "10-year ASCVD risk",
               ascvd_risk_30yr  = "30-year ASCVD risk",
               age  = "Age",
               sex  = "Sex",
